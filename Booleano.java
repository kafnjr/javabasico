public class Booleano {
	public static void main(String[] args){
		System.out.println(4 > 6);
		System.out.println(4 < 6);
		
		System.out.println(4 == 6);
		System.out.println(4 != 6);
		
		System.out.println(4 >= 6);
		System.out.println(4 <= 6);
		
		int valor1 = 6;
		int valor2 = 4;
		System.out.println(valor1 > valor2);
	}
}